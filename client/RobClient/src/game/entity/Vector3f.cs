namespace RobClient.Game.Entity {
    public class Vector3f {
        public float X
        { get; set; }

        public float Y
        { get; set; }

        public float Z
        { get; set; }

        public Vector3f(float x, float y, float z)
        {
            X = x;
            Y = y;
            Z = z;
        }
    }
}